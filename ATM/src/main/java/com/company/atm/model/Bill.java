package com.company.atm.model;

public class Bill {

	int currencyValue;

	int currencyCount;

	int currencyTotal;

	/**
	 * @return the currencyValue
	 */
	public int getCurrencyValue() {
		return currencyValue;
	}

	/**
	 * @param currencyValue the currencyValue to set
	 */
	public void setCurrencyValue(int currencyValue) {
		this.currencyValue = currencyValue;
	}

	/**
	 * @return the currencyCount
	 */
	public int getCurrencyCount() {
		return currencyCount;
	}

	/**
	 * @param currencyCount the currencyCount to set
	 */
	public void setCurrencyCount(int currencyCount) {
		this.currencyCount = currencyCount;
	}

	/**
	 * @return the currencyTotal
	 */
	public int getCurrencyTotal() {
		return currencyTotal;
	}

	/**
	 * @param currencyTotal the currencyTotal to set
	 */
	public void setCurrencyTotal(int currencyTotal) {
		this.currencyTotal = currencyTotal;
	}

}
